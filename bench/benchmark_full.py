def test_1_raw_speed():
    """Test raw rendering speed across document complexities."""
    print_header("1. Raw Rendering Speed (cold Engine per call)")

    cases = [
        ("simple", SIMPLE_HTML),
        ("invoice", INVOICE_HTML),
        ("complex (dashboard)", COMPLEX_HTML),
        ("stress (100 rows)", STRESS_HTML),
    ]

    for name, html in cases:
        times = bench(lambda h=html: ferropdf.from_html(h))
        pdf = ferropdf.from_html(html)
        pages = pdf.count(b"/Type /Page\n")
        size_kb = len(pdf) / 1024
        print_row(name, times, f"  [{pages}p, {size_kb:.0f}KB]")


def test_2_engine_reuse():
    """Compare cold start vs cached Engine."""
    print_header("2. Engine Reuse (cached fonts)")

    engine = ferropdf.Engine(ferropdf.Options(margin="15mm"))

    # Cold: new engine each time
    cold_times = bench(lambda: ferropdf.from_html(INVOICE_HTML))

    # Warm: reuse engine
    warm_times = bench(lambda: engine.render(INVOICE_HTML))

    print_row("cold (new Engine)", cold_times)
    print_row("warm (cached)", warm_times)
    speedup = statistics.median(cold_times) / statistics.median(warm_times)
    print(f"  {'cache speedup':<22} {speedup:.1f}x faster with Engine reuse")


def test_3_concurrent():
    """Test concurrent rendering with thread pool."""
    print_header("3. Concurrent Rendering (ThreadPool)")

    engine = ferropdf.Engine(ferropdf.Options(margin="15mm"))
    n_docs = 50

    for n_threads in [1, 2, 4]:
        start = time.perf_counter()
        with ThreadPoolExecutor(max_workers=n_threads) as pool:
            futures = [pool.submit(engine.render, INVOICE_HTML) for _ in range(n_docs)]
            results = [f.result() for f in as_completed(futures)]
        elapsed = time.perf_counter() - start
        per_doc = elapsed / n_docs
        throughput = n_docs / elapsed
        assert all(r[:4] == b"%PDF" for r in results)
        print(f"  {n_threads} thread(s), {n_docs} docs:  "
              f"total={fmt_ms(elapsed)}, per_doc={fmt_ms(per_doc)}, "
              f"throughput={throughput:.0f} docs/s")


