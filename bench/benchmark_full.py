# ── Benchmarking helpers ─────────────────────────────────────────────────────

WARMUP = 3
ITERATIONS = 30


def bench(fn, iterations=ITERATIONS, warmup=WARMUP):
    """Run fn() with warmup, return list of elapsed times in seconds."""
    for _ in range(warmup):
        fn()
    gc.collect()
    times = []
    for _ in range(iterations):
        start = time.perf_counter()
        fn()
        elapsed = time.perf_counter() - start
        times.append(elapsed)
    return times


def fmt_ms(seconds):
    return f"{seconds * 1000:.2f}ms"


def fmt_stats(times):
    med = statistics.median(times)
    std = statistics.stdev(times) if len(times) > 1 else 0
    mn = min(times)
    mx = max(times)
    p95 = sorted(times)[int(len(times) * 0.95)]
    return f"{fmt_ms(med)} median  (min={fmt_ms(mn)}, p95={fmt_ms(p95)}, max={fmt_ms(mx)}, std={fmt_ms(std)})"


def print_header(title):
    print()
    print(f"{'=' * 70}")
    print(f"  {title}")
    print(f"{'=' * 70}")


def print_row(label, times, extra=""):
    print(f"  {label:<22} {fmt_stats(times)}{extra}")


