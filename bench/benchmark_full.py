def test_5_framework_simulation():
    """Simulate FastAPI and Django PDF generation patterns."""
    print_header("5. Framework Integration Simulation")

    # FastAPI pattern: engine reused, run_in_executor
    engine = ferropdf.Engine(ferropdf.Options(margin="15mm"))

    import asyncio

    async def fastapi_render(html):
        loop = asyncio.get_event_loop()
        return await loop.run_in_executor(None, engine.render, html)

    async def run_fastapi_bench():
        # Warmup
        for _ in range(WARMUP):
            await fastapi_render(INVOICE_HTML)
        times = []
        for _ in range(ITERATIONS):
            start = time.perf_counter()
            pdf = await fastapi_render(INVOICE_HTML)
            elapsed = time.perf_counter() - start
            times.append(elapsed)
            assert pdf[:4] == b"%PDF"
        return times

    fastapi_times = asyncio.run(run_fastapi_bench())
    print_row("FastAPI (async)", fastapi_times)

    # Django pattern: new engine per request (worst case)
    def django_render(html):
        opts = ferropdf.Options(margin="15mm")
        eng = ferropdf.Engine(opts)
        return eng.render(html)

    django_times = bench(lambda: django_render(INVOICE_HTML))
    print_row("Django (per-request)", django_times)

    # Django pattern: shared engine (best case)
    shared_engine = ferropdf.Engine(ferropdf.Options(margin="15mm"))
    django_shared_times = bench(lambda: shared_engine.render(INVOICE_HTML))
    print_row("Django (shared eng)", django_shared_times)

    # FastAPI concurrent requests simulation
    async def concurrent_fastapi(n_requests):
        tasks = [fastapi_render(INVOICE_HTML) for _ in range(n_requests)]
        return await asyncio.gather(*tasks)

    n_req = 20
    start = time.perf_counter()
    results = asyncio.run(concurrent_fastapi(n_req))
    elapsed = time.perf_counter() - start
    assert all(r[:4] == b"%PDF" for r in results)
    print(f"  {'FastAPI burst':<22} {n_req} concurrent: total={fmt_ms(elapsed)}, "
          f"per_req={fmt_ms(elapsed/n_req)}, throughput={n_req/elapsed:.0f} req/s")


def test_6_vs_weasyprint():
    """Head-to-head comparison with WeasyPrint."""
    print_header("6. ferropdf vs WeasyPrint")

    if WeasyHTML is None:
        print("  WeasyPrint not installed — skipping.")
        print("  Install with: pip install weasyprint")
        return

    cases = [
        ("simple", SIMPLE_HTML),
        ("invoice", INVOICE_HTML),
        ("complex", COMPLEX_HTML),
        ("stress (100 rows)", STRESS_HTML),
    ]

    engine = ferropdf.Engine(ferropdf.Options(margin="15mm"))

    results_table = []

    for name, html in cases:
        ferro_times = bench(lambda h=html: engine.render(h))
        weasy_times = bench(lambda h=html: WeasyHTML(string=h).write_pdf(), iterations=10, warmup=2)

        ferro_med = statistics.median(ferro_times)
        weasy_med = statistics.median(weasy_times)
        speedup = weasy_med / ferro_med

        results_table.append((name, ferro_med, weasy_med, speedup))

        print(f"\n  -- {name} --")
        print_row("ferropdf", ferro_times)
        print_row("weasyprint", weasy_times)
        print(f"  {'SPEEDUP':<22} {speedup:.1f}x faster")

    # Summary table
    print(f"\n  {'─' * 66}")
    print(f"  {'Document':<22} {'ferropdf':>10} {'weasyprint':>12} {'speedup':>10}")
    print(f"  {'─' * 66}")
    for name, ferro_med, weasy_med, speedup in results_table:
        print(f"  {name:<22} {fmt_ms(ferro_med):>10} {fmt_ms(weasy_med):>12} {speedup:>9.1f}x")
    print(f"  {'─' * 66}")
    avg_speedup = statistics.mean(s for _, _, _, s in results_table)
    print(f"  {'AVERAGE':<22} {'':>10} {'':>12} {avg_speedup:>9.1f}x")


